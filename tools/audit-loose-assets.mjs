// ───────────────────────────────────────────────────────────────────────────
//  audit-loose-assets.mjs — scan the repo root and ./.staging for every loose
//  3D-asset source (and its texture siblings) and report where each one would
//  be routed in the served public/assets/models tree.
//
//  Run:  npm run audit:loose   (or: node tools/audit-loose-assets.mjs)
//  Out:  docs/LOOSE_ASSET_AUDIT.md
//
//  This is a READ-ONLY audit — it never moves, converts, or deletes anything.
//  Use `npm run organize` to actually route staged packs.
// ───────────────────────────────────────────────────────────────────────────
import fs from 'node:fs';
import path from 'node:path';
import { routeFor } from './asset-routing.mjs';

const ROOT = process.cwd();
const OUT_DOC = path.join(ROOT, 'docs', 'LOOSE_ASSET_AUDIT.md');

const MODEL_EXT = ['.glb', '.gltf', '.fbx', '.obj', '.dae', '.blend'];
const TEX_EXT = ['.png', '.jpg', '.jpeg', '.webp'];

// Folders at the repo root we must never treat as asset sources.
const SKIP_DIRS = new Set([
  'node_modules', 'dist', '.git', '.github', 'src', 'docs', 'scripts', 'tools',
  'vendor', 'public', 'assets', '.uploads',
]);

function walk(dir, acc = []) {
  let entries;
  try { entries = fs.readdirSync(dir, { withFileTypes: true }); } catch { return acc; }
  for (const e of entries) {
    const p = path.join(dir, e.name);
    if (e.isDirectory()) {
      if (dir === ROOT && SKIP_DIRS.has(e.name)) continue;
      walk(p, acc);
    } else {
      acc.push(p);
    }
  }
  return acc;
}

function human(bytes) {
  if (!bytes || bytes <= 0) return '—';
  const u = ['B', 'KB', 'MB', 'GB']; let i = 0, n = bytes;
  while (n >= 1024 && i < u.length - 1) { n /= 1024; i++; }
  return `${n.toFixed(n < 10 && i > 0 ? 1 : 0)} ${u[i]}`;
}

// Gather candidate source roots: the repo root (loose top-level files/folders)
// plus the gitignored ./.staging tree.
const roots = [ROOT, path.join(ROOT, '.staging')];
const seen = new Set();
const models = [];
const textures = [];
for (const r of roots) {
  for (const f of walk(r)) {
    if (seen.has(f)) continue;
    seen.add(f);
    const ext = path.extname(f).toLowerCase();
    if (MODEL_EXT.includes(ext)) models.push(f);
    else if (TEX_EXT.includes(ext)) textures.push(f);
  }
}

const esc = (s) => String(s == null ? '' : s).replace(/\\/g, '\\\\').replace(/\|/g, '\\|');

const rows = models.map((abs) => {
  const rel = path.relative(ROOT, abs).split(path.sep).join('/');
  const ext = path.extname(abs).toLowerCase();
  // route by the pack folder name first, then fall back to the file name.
  const packName = path.relative(ROOT, path.dirname(abs)).split(path.sep).filter(Boolean).pop() || path.basename(abs);
  const route = routeFor(packName) || routeFor(path.basename(abs));
  let bytes = 0; try { bytes = fs.statSync(abs).size; } catch { /* ignore */ }
  let action, notes;
  if (!route) {
    action = 'review';
    notes = 'no routing rule matched — extend tools/asset-routing.mjs';
  } else if (ext === '.glb' || ext === '.gltf') {
    action = 'copy';
    notes = 'web-ready: copy into served tree';
  } else {
    action = 'convert';
    notes = `convert ${ext} → .glb (assimp) and embed textures`;
  }
  return {
    rel, ext, bytes,
    category: route ? route.dest : 'unknown',
    dest: route ? `public/assets/models/${route.dest}/` : '—',
    action, notes,
  };
}).sort((a, b) => a.category.localeCompare(b.category) || a.rel.localeCompare(b.rel));

let md = '';
md += '# Loose Asset Audit\n\n';
md += `_Generated by \`tools/audit-loose-assets.mjs\` (\`npm run audit:loose\`). Read-only audit of loose 3D-asset sources in the repo root and \`./.staging\`. Re-run after dropping new packs._\n\n`;
md += '## Summary\n\n';
md += `- Model sources found: **${models.length}**\n`;
md += `- Texture siblings found: **${textures.length}**\n`;
md += `- Routable (rule matched): **${rows.filter(r => r.action !== 'review').length}**\n`;
md += `- Needs review (no rule): **${rows.filter(r => r.action === 'review').length}**\n\n`;

md += '## Model sources\n\n';
if (!rows.length) {
  md += '_None found. Drop packs into `./.staging` or the repo root, then re-run `npm run audit:loose`._\n\n';
} else {
  md += '| Source path | Guessed category | Destination | Action | Notes |\n';
  md += '|---|---|---|---|---|\n';
  for (const r of rows) {
    md += `| ${esc(r.rel)} | ${esc(r.category)} | ${esc(r.dest)} | ${esc(r.action)} | ${esc(r.notes)} |\n`;
  }
  md += '\n';
}

if (textures.length) {
  md += '## Texture siblings (kept with their model — never delete)\n\n';
  md += '| Texture path | Size |\n|---|---:|\n';
  for (const t of textures.sort()) {
    const rel = path.relative(ROOT, t).split(path.sep).join('/');
    let b = 0; try { b = fs.statSync(t).size; } catch { /* ignore */ }
    md += `| ${esc(rel)} | ${human(b)} |\n`;
  }
  md += '\n';
}

md += '## Notes\n\n';
md += '- This audit never moves or deletes files; run `npm run organize` to route staged packs.\n';
md += '- Root-level raw sources (`*.zip/*.rar/*.obj/*.blend/*.fbx`) are gitignored, so commit lean converted GLBs under `public/assets/models/**` rather than the heavy originals.\n';
md += '- Do **not** delete `.bin`, textures, or credit/license files.\n';

fs.mkdirSync(path.dirname(OUT_DOC), { recursive: true });
fs.writeFileSync(OUT_DOC, md);
console.log(`Wrote ${path.relative(ROOT, OUT_DOC)} — ${models.length} model sources, ${textures.length} textures, ${rows.filter(r => r.action === 'review').length} need review.`);
