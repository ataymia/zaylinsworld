// One-off generator for docs/ASSET_USAGE_REPORT.md.
//   node scripts/gen-asset-report.mjs
// Classifies every asset via src/config/assetRegistry.js, checks whether each is
// referenced in src/ code or the manifest, measures file size, and flags
// unused/large/unsafe assets. Does NOT delete anything.
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { buildRegistry } from '../src/config/assetRegistry.js';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const indexPath = path.join(root, 'public/assets/models/asset-index-v2.json');
const modelsDir = path.join(root, 'public/assets/models');

const index = JSON.parse(fs.readFileSync(indexPath, 'utf8'));
const registry = buildRegistry(index);

// Gather all source text once so we can substring-search for asset names.
function readAll(dir, exts, acc = []) {
  for (const e of fs.readdirSync(dir, { withFileTypes: true })) {
    const p = path.join(dir, e.name);
    if (e.isDirectory()) { if (!/node_modules|dist|\.git/.test(p)) readAll(p, exts, acc); }
    else if (exts.some(x => e.name.endsWith(x))) acc.push(fs.readFileSync(p, 'utf8'));
  }
  return acc;
}
const srcText = readAll(path.join(root, 'src'), ['.js']).join('\n');
let manifestText = '';
for (const m of ['src/config/manifest.json', 'assets/manifest.json']) {
  const mp = path.join(root, m);
  if (fs.existsSync(mp)) manifestText += fs.readFileSync(mp, 'utf8') + '\n';
}

function sizeOf(relPath) {
  const fp = path.join(modelsDir, relPath.replace(/^models\//, ''));
  try { return fs.statSync(fp).size; } catch { return 0; }
}
function human(bytes) {
  if (bytes <= 0) return '—';
  const u = ['B', 'KB', 'MB']; let i = 0, n = bytes;
  while (n >= 1024 && i < u.length - 1) { n /= 1024; i++; }
  return `${n.toFixed(n < 10 && i > 0 ? 1 : 0)} ${u[i]}`;
}

const rows = registry.map(a => {
  const usedCode = srcText.includes(a.name);
  const usedManifest = manifestText.includes(a.name);
  const bytes = sizeOf(a.path);
  const live = a.tested || usedCode;
  return { ...a, usedCode, usedManifest, bytes, live };
});

// Stats
const byDomain = {};
for (const r of rows) (byDomain[r.domain] ||= []).push(r);
const totalBytes = rows.reduce((s, r) => s + r.bytes, 0);
const usedCount = rows.filter(r => r.usedCode || r.usedManifest).length;
const liveCount = rows.filter(r => r.live).length;

// Large unused (candidate-for-review, NOT delete) ≥ 2 MB and unreferenced.
const largeUnused = rows
  .filter(r => !r.usedCode && !r.usedManifest && r.bytes >= 2 * 1024 * 1024)
  .sort((a, b) => b.bytes - a.bytes);

const esc = (s) => String(s == null ? '' : s).replace(/\|/g, '\\|');
const yn = (b) => (b ? 'yes' : 'no');

let md = '';
md += '# Asset Usage Report\n\n';
md += `_Generated by \`scripts/gen-asset-report.mjs\` from \`public/assets/models/asset-index-v2.json\` and \`src/config/assetRegistry.js\`. Do not edit by hand — re-run the script._\n\n`;
md += '## Summary\n\n';
md += `- Total indexed assets: **${rows.length}**\n`;
md += `- Total size (indexed models): **${human(totalBytes)}**\n`;
md += `- Referenced in code or manifest: **${usedCount}**\n`;
md += `- Live / tested in gameplay: **${liveCount}**\n`;
md += `- Large (≥2 MB) AND unreferenced (review candidates, NOT auto-delete): **${largeUnused.length}**\n\n`;

md += '## By domain\n\n';
md += '| Domain | Assets | Referenced | Size |\n|---|---:|---:|---:|\n';
for (const d of Object.keys(byDomain).sort()) {
  const list = byDomain[d];
  const ref = list.filter(r => r.usedCode || r.usedManifest).length;
  const sz = list.reduce((s, r) => s + r.bytes, 0);
  md += `| ${d} | ${list.length} | ${ref} | ${human(sz)} |\n`;
}
md += '\n';

md += '## Safety rules (read before deleting anything)\n\n';
md += '- **Do NOT** delete `.bin` files paired with a `.gltf`.\n';
md += '- **Do NOT** delete textures referenced by a GLTF/GLB.\n';
md += '- **Do NOT** delete license / credit files.\n';
md += '- **Do NOT** delete any asset marked `live=yes` or referenced in code/manifest.\n';
md += '- "Large + unreferenced" below are **review candidates only** — confirm by hand first.\n\n';

md += '## Large unreferenced assets (review candidates)\n\n';
if (!largeUnused.length) md += '_None._\n\n';
else {
  md += '| Asset | Category | Path | Size |\n|---|---|---|---:|\n';
  for (const r of largeUnused) md += `| ${esc(r.name)} | ${esc(r.category)} | ${esc(r.path)} | ${human(r.bytes)} |\n`;
  md += '\n';
}

md += '## Full asset inventory\n\n';
md += '| Asset | Domain | Category | Pack | Size | In code | In manifest | Live | Safe | Placement | Interact |\n';
md += '|---|---|---|---|---:|:--:|:--:|:--:|:--:|---|:--:|\n';
for (const r of rows.sort((a, b) => a.domain.localeCompare(b.domain) || a.category.localeCompare(b.category) || a.name.localeCompare(b.name))) {
  md += `| ${esc(r.name)} | ${esc(r.domain)} | ${esc(r.category)} | ${esc(r.pack)} | ${human(r.bytes)} | ${yn(r.usedCode)} | ${yn(r.usedManifest)} | ${yn(r.live)} | ${yn(r.safe)} | ${esc(r.placement)} | ${yn(r.interactable)} |\n`;
}
md += '\n';

const outDir = path.join(root, 'docs');
fs.mkdirSync(outDir, { recursive: true });
fs.writeFileSync(path.join(outDir, 'ASSET_USAGE_REPORT.md'), md);
console.log(`Wrote docs/ASSET_USAGE_REPORT.md — ${rows.length} assets, ${human(totalBytes)}, ${usedCount} referenced, ${largeUnused.length} large-unused.`);
